        // Sends new message
        sendMessage() {

            // Message validation
            if (this.newMessage.trim() == "") {
                return;
            }

            // Create date instance
            let date = luxon.DateTime.now();
            date = date.toFormat(this.dateFormat).toString();

            // Message sending
            this.activeContact.messages.push({
                date: date,
                message: this.newMessage,
                status: "sent",
            });

            // Send mock response
            setTimeout(() => {
                this.activeContact.messages.push({
                    date: date,
                    message: "ok",
                    status: "received",
                });
            }, 1000);

            // Clear message
            this.newMessage = "";
        },

